function move(step, position) {
  const [ direction ] = Object.keys(step);
  const [value ] = Object.values(step);

  switch (direction) {
    case 'forward':
      position.x += value;
      break;
    case 'down':
      position.y += value;
      break;
    case 'up':
      position.y -= value;
      break;
    default:
      console.log("Something went wrong...");
  }
};

